# Büyükten küçüğe sort fonksiyonu ile
sayilar = [5, 2, 9, 1, 7]
sayilar.sort(reverse=True)
print(sayilar)  
# Output: [9, 7, 5, 2, 1]

# Küçükten büyüğe sort fonksiyonu ile
sayilar = [5, 2, 9, 1, 7]
sayilar.sort()
print(sayilar)  
# Output: [1, 2, 5, 7, 9]

# Büyükten küçüğe sorted fonksiyonu ile
sayilar = [5, 2, 9, 1, 7]
sorted_sayilar = sorted(sayilar, reverse=True)
print(sorted_sayilar)  
# Output: [9, 7, 5, 2, 1]

# Küçükten büyüğe sorted fonksiyonu ile
sayilar = [5, 2, 9, 1, 7]
sorted_sayilar = sorted(sayilar)
print(sorted_sayilar)  
# Output: [1, 2, 5, 7, 9]
